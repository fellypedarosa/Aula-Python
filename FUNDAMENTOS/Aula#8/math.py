#Funcionalidades da biblioteca math
# 
#   Funções Matemáticas
#   Funções Trigonométricas:
#   
#   math.sin(x): Seno de x (x em radianos)
#   math.cos(x): Cosseno de x (x em radianos)
#   math.tan(x): Tangente de x (x em radianos)
#   math.asin(x): Arco-seno de x (resultado em radianos)
#   math.acos(x): Arco-cosseno de x (resultado em radianos)
#   math.atan(x): Arco-tangente de x (resultado em radianos)
#   math.atan2(y, x): Arco-tangente de y/x levando em conta os sinais de ambos os argumentos para determinar o quadrante correto.
#   Funções Hiperbólicas:
#   
#   math.sinh(x): Seno hiperbólico de x
#   math.cosh(x): Cosseno hiperbólico de x
#   math.tanh(x): Tangente hiperbólica de x
#   math.asinh(x): Arco-seno hiperbólico de x
#   math.acosh(x): Arco-cosseno hiperbólico de x
#   math.atanh(x): Arco-tangente hiperbólico de x
#   Funções Exponenciais e Logarítmicas:
#   
#   math.exp(x): Exponencial de x (e^x)
#   math.log(x, base): Logaritmo de x na base especificada. Se a base não for especificada, calcula o logaritmo natural (base e).
#   math.log1p(x): Logaritmo natural de (1 + x)
#   math.log2(x): Logaritmo na base 2 de x
#   math.log10(x): Logaritmo na base 10 de x

#   Funções de Potência e Raiz:
#   
#   math.pow(x, y): x elevado à potência y
#   math.sqrt(x): Raiz quadrada de x
#   math.cbrt(x): Raiz cúbica de x (disponível a partir do Python 3.11)
#   math.hypot(x, y): Hipotenusa (ou norma Euclidiana) para os valores x e y
#   Funções de Arredondamento e Absoluto:
#   
#   math.ceil(x): Arredonda x para o inteiro mais próximo para cima
#   math.floor(x): Arredonda x para o inteiro mais próximo para baixo
#   math.trunc(x): Trunca x para o inteiro mais próximo em direção a zero
#   math.fabs(x): Valor absoluto de x
#   math.fmod(x, y): Retorna o resto da divisão de x por y
#   math.remainder(x, y): Calcula o resto IEEE 754 da divisão de x por y
#   Funções de Fatores e Combinatórias:
#   
#   math.factorial(x): Fatorial de x
#   math.comb(n, k): Número de combinações de n itens tomados k a k (disponível a partir do Python 3.8)
#   math.perm(n, k): Número de permutações de n itens tomados k a k (disponível a partir do Python 3.8)
#   Funções Especiais:
#   
#   math.gamma(x): Função Gamma de x
#   math.lgamma(x): Logaritmo natural da função Gamma de x
#   math.erf(x): Função erro de x
#   math.erfc(x): Complemento da função erro de x
#   math.tau(x): Valor da função Beta incompleta (disponível a partir do Python 3.11)
#   Constantes Matemáticas
#   math.pi: Constante pi (3.141592...)
#   math.e: Constante e (2.71828...)
#   math.tau: Constante τ (6.283185...) que é 2*pi
#   math.inf: Representa o infinito
#   math.nan: Representa "not a number" (não um número)


































































